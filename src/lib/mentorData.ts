// 특정 Day의 멘토를 가져오는 유틸리티
export const getMentorForDay = (day: number): Mentor | undefined => {
    return MENTORS.find(m => m.days.includes(day));
};

// 모든 명언을 평탄화한 리스트 (총 150개)
export const getAllQuotes = (): { mentor: string; text: string }[] => {
    const results: { mentor: string; text: string }[] = [];
    MENTORS.forEach(m => {
        m.quotes.forEach(q => results.push({ mentor: m.name, text: q }));
        m.transcriptions.forEach(t => results.push({ mentor: m.name, text: t }));
    });
    return results;
};

// Phase별 멘토 필터
export const getMentorsByPhase = (phase: Mentor['phase']): Mentor[] => {
    return MENTORS.filter(m => m.phase === phase);
};
